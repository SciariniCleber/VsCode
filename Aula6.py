# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool
print(int('1'), type(int('1'))) # int é considerado uma funçao neste caso em especifico, mais ele e uma class
print(type(float('1') + 1))
print(bool(' '))
print(str(11) + 'b')
print(1 + 1)
print('a' + 'b')
print(type(int('1') + 1))
print(int('1') + 2)
print(float('2.3') + 5)
print(bool(10 == 12))
print(bool('Amor' + 'zinho'))
print('Amor' + 'zinho')
print(bool(32))